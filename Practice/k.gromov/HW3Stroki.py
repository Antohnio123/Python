print("Введите строку из двух слов:")

s = input()
word1 = s[:s.find(' ')]  # первое слово до пробела
word2 = s[s.find(' ') + 1:]  # второе слово после пробела (пробел - нулевой индекс)
print(word2 + ' ' + word1)
